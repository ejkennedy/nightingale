// London wall-clock slot times per practitioner type.
const GP_TIMES = ['09:00', '09:10', '09:20', '09:30', '11:00', '11:10', '15:00', '15:10'];
const DENTIST_TIMES = ['09:00', '09:20', '09:40', '14:00', '14:20'];

const esc = (s: string) => s.replace(/'/g, "''");

function build(): string {
  const lines: string[] = [
    '-- GENERATED by scripts/generate-seed.ts — do not edit by hand.',
    '-- Synthetic demo data. Re-run `bun run db:generate-seed` to refresh dates.',
    'PRAGMA foreign_keys = ON;',
    'DELETE FROM emails;',
    'DELETE FROM events;',
    'DELETE FROM escalations;',
    'DELETE FROM appointments;',
    'DELETE FROM slots;',
    'DELETE FROM call_logs;',
    'DELETE FROM patients;',
    'DELETE FROM practitioners;',
    '',
  ];

  for (const p of practitioners) {
    lines.push(
      `INSERT INTO practitioners (id, name, role, specialty) VALUES ('${p.id}', '${esc(p.name)}', '${p.role}', '${esc(p.specialty)}');`,
    );
  }
  lines.push('');
  for (const p of patients) {
    lines.push(
      `INSERT INTO patients (id, first_name, last_name, dob, phone, email) VALUES ('${p.id}', '${esc(p.first)}', '${esc(p.last)}', '${p.dob}', '${p.phone}', '${esc(p.email)}');`,
    );
  }
  lines.push('');

  const today = new Date();
  let added = 0;
  for (let dayOffset = 1; dayOffset <= 20 && added < 14; dayOffset++) {
    const d = new Date(
      Date.UTC(today.getUTCFullYear(), today.getUTCMonth(), today.getUTCDate() + dayOffset),
    );
    const dow = d.getUTCDay();
    if (dow === 0 || dow === 6) continue; // skip weekends
    added++;
    const [y, mo, da] = [d.getUTCFullYear(), d.getUTCMonth() + 1, d.getUTCDate()];
    const stamp = `${y}${String(mo).padStart(2, '0')}${String(da).padStart(2, '0')}`;
    for (const pr of practitioners) {
      const times = pr.role === 'Dentist' ? DENTIST_TIMES : GP_TIMES;
      const dur = pr.role === 'Dentist' ? 20 : 10;
      for (const t of times) {
        const [h, mi] = t.split(':').map(Number);
        const iso = londonToUtcIso(y, mo, da, h!, mi!);
        const id = `slot-${pr.id}-${stamp}-${t.replace(':', '')}`;
        lines.push(
          `INSERT INTO slots (id, practitioner_id, starts_at, duration_minutes, status) VALUES ('${id}', '${pr.id}', '${iso}', ${dur}, 'available');`,
        );
      }
    }
  }
  lines.push('');
  return lines.join('\n');
}

writeFileSync(new URL('../src/db/seed.sql', import.meta.url), build());
