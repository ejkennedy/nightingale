import { writeFileSync } from 'node:fs';

const TZ = 'Europe/London';

/** Minutes Europe/London is ahead of UTC at a given instant (handles BST/GMT). */
function londonOffsetMinutes(utc: Date): number {
  const parts = new Intl.DateTimeFormat('en-GB', {
    timeZone: TZ,
    hour12: false,
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit',
  })
    .formatToParts(utc)
    .reduce<Record<string, string>>((a, p) => ((a[p.type] = p.value), a), {});
  const asUtc = Date.UTC(
    +parts.year!,
    +parts.month! - 1,
    +parts.day!,
    +parts.hour!,
    +parts.minute!,
    +parts.second!,
  );
  return (asUtc - utc.getTime()) / 60000;
}

/** London wall-clock (y,m,d,h,mi) -> stored UTC ISO string. */
function londonToUtcIso(y: number, mo: number, d: number, h: number, mi: number): string {
  let ts = Date.UTC(y, mo - 1, d, h, mi);
  for (let i = 0; i < 2; i++)
    ts = Date.UTC(y, mo - 1, d, h, mi) - londonOffsetMinutes(new Date(ts)) * 60000;
  return new Date(ts).toISOString().replace('.000Z', 'Z');
}

